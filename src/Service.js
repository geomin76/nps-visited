function TabPanel(props) {
    const { children, value, index, ...other } = props;

    return (
        <div
            role="tabpanel"
            hidden={value !== index}
            id={`simple-tabpanel-${index}`}
            aria-labelledby={`simple-tab-${index}`}
            {...other}
        >
            {value === index && (
                <>
                    {children}
                </>
            )}
        </div>
    );
}

function a11yProps(index) {
    return {
        id: `simple-tab-${index}`,
        'aria-controls': `simple-tabpanel-${index}`,
    };
}

const countVisitedParks = (parks) => {
    let visited = 0;
    parks.map((innerArr) => {
        innerArr.map((innerElem) => {
            if (innerElem.visited === true) visited++;
        })
    })
    return visited;
}

const setVisited = (index, setData) => {
    const ELEMENT_DIVIDED_BY = 9;

    setData(prev => {
        const newData = [...prev]
        const column = Math.floor(index / ELEMENT_DIVIDED_BY)
        const row = index % ELEMENT_DIVIDED_BY
        const prevStatus = newData[column][row].visited
        newData[column][row].visited = !prevStatus;
        return newData;
    })
}

